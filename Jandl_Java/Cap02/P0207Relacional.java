package com.company.Jandl_Java.Cap02;

public class P0207Relacional {

	public static void main(String[] args) {
		
		int a = 15, b = 12;
		
		System.out.println("Valores: a = "+ a + ", b ="+b);
		
		System.out.println("a == b -> " + (a==b));
		System.out.println("a != b -> " + (a!=b));
		System.out.println("a < b -> " + (a<b));
		System.out.println("a > b -> " + (a<b));
		System.out.println("a <= b ->" + (a <= b));
		System.out.println("a >= b -> " + (a >= b));
		
	}
}
